CREATE DATABASE EXERCICIO2
USE EXERCICIO2
CREATE TABLE VENDEDOR(idVendedor INTEGER PRIMARY KEY, NOME CHAR(30), TELEFONE DECIMAL, EMAIL CHAR(20), CIDADE CHAR(20), FOREIGN KEY REFERENCES IMOVEL(idImovel))
CREATE TABLE IMOVEL(idImovel INTEGER PRIMARY KEY)
CREATE TABLE BAIRRO(idBairro INTEGER PRIMARY KEY, NOME CHAR(30) NOT NULL, CONSTRAINT fk_Imovel FOREIGN KEY (idImovel) REFERENCES IMOVEL(idImovel))
CREATE TABLE CIDADE(idCidade INTEGER PRIMARY KEY, NOME CHAR(30) NOT NULL, FOREIGN KEY REFERENCES BAIRRO(idBairro))
CREATE TABLE ESTADO(idEstado INTEGER PRIMARY KEY, NOME CHAR(30) NOT NULL, FOREIGN KEY REFERENCES CIDADE(idCidade))
CREATE TABLE OFERTA(idOferta INTEGER PRIMARY KEY, PRECO INTEGER, FOREIGN KEY REFERENCES COMPRADOR(idComprador), FOREIGN KEY REFERENCES IMOVEL(idImovel))
CREATE TABLE COMPRADOR(idComprador INTEGER PRIMARY KEY, NOME CHAR(30), ENDERECO CHAR(30), TELEFONE INTEGER, EMAIL CHAR(40))
